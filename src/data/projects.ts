// Helper function to get project by ID
export function getProjectById(id: string): Project | undefined {
  return PORTFOLIO_PROJECTS.find(p => p.id === id);
}

// Helper function to get projects by brand
export function getProjectsByBrand(brandId: string): Project[] {
  return PORTFOLIO_PROJECTS.filter(p => p.brand_id === brandId);
}

// Helper function to get featured projects
export function getFeaturedProjects(): Project[] {
  return PORTFOLIO_PROJECTS.filter(p => p.featured);
}

// Helper function to get projects by industry
export function getProjectsByIndustry(industry: string): Project[] {
  return PORTFOLIO_PROJECTS.filter(p =>
    p.industry?.toLowerCase().includes(industry.toLowerCase())
  );
}

export default PORTFOLIO_PROJECTS;
